package expresscorreos.model;

public class Incluye_AE {
    private int id_ae;
    private int incluye_ae;

    public Incluye_AE(int id_ae, int incluye_ae) {
        this.id_ae = id_ae;
        this.incluye_ae = incluye_ae;
    }

    public int getId_ae() {
        return id_ae;
    }

    public int getIncluye_ae() {
        return incluye_ae;
    }

}